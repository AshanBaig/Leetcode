    def checkSubarraySum(self, nums, k: int) -> bool:
        print( [0] * (len(nums) + 1))
        for i in range(len(nums)):
            for j in range(i+1,len(nums)):
                if sum(nums[i:j+1])%k==0:
                    return True
        return False
            
